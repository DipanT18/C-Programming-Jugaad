#include<stdio.h>
int main(){
    int a = 8, b = -3;
    printf("Original values: a = %d, b = %d\n", a, b);
    printf("Unary plus applied to a: %d\n", +a);
    printf("Unary minus applied to a: %d\n", -a);
    printf("Unary plus applied to b: %d\n", +b);
    printf("Unary minus applied to b: %d\n", -b);
    return 0;
}